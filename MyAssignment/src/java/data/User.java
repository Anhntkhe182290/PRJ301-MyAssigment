import java.util.List;

public class User {

    private String username;
    private String password;
    private String fullName;
    private String dob;
    private int gender; // 1: Male, 0: Female
    private String depid;
    private List<Role> roles; // Danh sách quyền

    public User(String username, String password, String fullName, String dob, int gender, String depid, List<Role> roles) {
        this.username = username;
        this.password = password;
        this.fullName = fullName;
        this.dob = dob;
        this.gender = gender;
        this.depid = depid;
        this.roles = roles;
    }

    // Getters và Setters
    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public int getGender() {
        return gender;
    }

    public void setGender(int gender) {
        this.gender = gender;
    }

    public String getDepid() {
        return depid;
    }

    public void setDepid(String depid) {
        this.depid = depid;
    }

    public List<Role> getRoles() {
        return roles;
    }

    public void setRoles(List<Role> roles) {
        this.roles = roles;
    }

    public boolean hasRole(String roleName) {
        return roles.stream().anyMatch(role -> role.getRname().equalsIgnoreCase(roleName));
    }
}
